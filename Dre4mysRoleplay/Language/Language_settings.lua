function isGerman(player)
	if(tonumber(getElementData(player,'Language')) == 0)then
		return true
	else
		return false
	end
end

function isEnglish(player)
	if(tonumber(getElementData(player,'Language')) == 1)then
		return true
	else
		return false
	end
end

function getChatBox(player,textid)
	if(isGerman(player))then
		infobox_func(player,German[textid],255,255,255)
	elseif(isEnglish(player))then
		infobox_func(player,English[textid],255,255,255)
	end
end
addEvent('getChatBox',true)
addEventHandler('getChatBox',root,getChatBox)

function changelanguage_func(player)
	if(isGerman(player))then
		setElementData(player,'Language',1)
		infobox_func(player,'You have change your Language to English.',255,255,255)
	elseif(isEnglish(player))then
		setElementData(player,'Language',0)
		infobox_func(player,'Du hast deine Sprache zu Deutsch geändert.',255,255,255)
	end
end
addCommandHandler('language',changelanguage_func)